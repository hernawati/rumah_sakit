    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:

        SimpleDateFormat sdf = new SimpleDateFormat("ddMMyyyy");
        Calendar cal = Calendar.getInstance();
        String tgl = sdf.format(cal.getTime());
        //String tgl = "04092013";

        String nomor = pds.generateNomorService(tgl);

        String hasilPasien = pss.serviceNamaByIdPasien(txtIdPasien.getText());

        String pilihDokter = (String) cmbNmDokter.getSelectedItem();
        String pilihJaminan = (String) cmbJaminan.getSelectedItem();

        String idDokter = ds.serviceTampilIdDokterByNama((pilihDokter));
        String idJaminan = js.getIdJaminanByNama(pilihJaminan);
        
        if (hasilPasien == null) {
            JOptionPane.showMessageDialog(rootPane, "Periksa kembali ID Pasien!","Validasi",JOptionPane.WARNING_MESSAGE);
        } else if ((pilihDokter == null) || (pilihDokter.equalsIgnoreCase("")) || (pilihDokter.equalsIgnoreCase(" "))) {
            JOptionPane.showMessageDialog(rootPane, "Dokter Tidak Boleh Kosong!","Validasi",JOptionPane.WARNING_MESSAGE);
        } else if (idDokter == null) {
            JOptionPane.showMessageDialog(rootPane, "Periksa kembali nama dokter!","Validasi",JOptionPane.WARNING_MESSAGE);
        } else if ((pilihJaminan == null) || (pilihJaminan.equalsIgnoreCase("")) || (pilihJaminan.equalsIgnoreCase(" "))) {
            JOptionPane.showMessageDialog(rootPane, "Jaminan Tidak Boleh Kosong!","Validasi",JOptionPane.WARNING_MESSAGE);
        } else if (idJaminan == null) {
            JOptionPane.showMessageDialog(rootPane, "Periksa kembali nama jaminan!","Validasi",JOptionPane.WARNING_MESSAGE);
        } else {
            //System.out.println(pds.generateNomorService(tgl));

            Pendaftaran p = new Pendaftaran();
            p.setNoReg(nomor);
            p.setIdPasien(txtIdPasien.getText());
            p.setIdDokter(idDokter);
            p.setIdJaminan(idJaminan);
            pds.insertPendaftaranService(p);
            clear();
//            JOptionPane.showMessageDialog(rootPane, "No Daftar  : " + nomor+"\n"
//                    + "ID Pasien  : " + txtIdPasien.getText()+"\n"
//                    +"ID Dokter  : " + idDokter+"\n"
//                    +"ID Jaminan : " + idJaminan);

//            System.out.println("No Daftar  : " + nomor);
//            System.out.println("ID Pasien  : " + txtIdPasien.getText());
//            System.out.println("ID Dokter  : " + idDokter);
//            System.out.println("ID Jaminan : " + idJaminan);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed
