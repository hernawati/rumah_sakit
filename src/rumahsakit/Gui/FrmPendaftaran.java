    public FrmPendaftaran() {
        initComponents();

        /*Mengisi data SPESIALIS di JComboBox dengan auto-complete*/
        tabelModelSpesialis.setData(ss.serviceGetAllSpesialis());
        int spesialis = tabelModelSpesialis.getRowCount();
        completeSpesialis = AutoCompleteSupport.install(
                cmbSpesialis, GlazedLists.eventListOf(ss.serviceGetNamaSpesialis(spesialis)));

        cmbSpesialis.addItemListener(new ItemListener() {

            public void itemStateChanged(ItemEvent e) {
                try {
                    /*Me-remove All item dengan setModel*/
                    String[] list = new String[]{""};
                    cmbNmDokter.setModel(new javax.swing.DefaultComboBoxModel(list));

                    /*Mengambil data pilihan spesialis*/
                    String nama = (String) cmbSpesialis.getSelectedItem();
                    String kodeSpesialis = ss.serviceGetIDSpesialis(nama);
                    //System.out.println("Kode spesialis = "+kodeSpesialis);

                    /*Mengisi pilihan nama dokter berdasarkan pilihan spesialis*/
                    tabelModelDokter.setData(ds.serviceGetByIdSpesialis(kodeSpesialis));
                    int b = tabelModelDokter.getRowCount();
                    completeDokter = AutoCompleteSupport.install(
                            cmbNmDokter, GlazedLists.eventListOf(ds.serviceNamaDokter(kodeSpesialis, b)));
                } catch (Throwable t) {
                }
            }
        });

        /*Mengisi data JAMINAN di JComboBox dengan auto-complete*/
        tabelModelJaminan.setData(js.serviceGetAllJaminan());
        int jaminan = tabelModelJaminan.getRowCount();
        completeJaminan = AutoCompleteSupport.install(
                cmbJaminan, GlazedLists.eventListOf(js.serviceTampilNamaJaminan(jaminan)));

    }

